package model;

public class ListKapalModel {

    private String idPemilik;
    private String idKapal;
    private String namaKapal;

    public String getIdPemilik() {
        return idPemilik;
    }

    public void setIdPemilik(String idPemilik) {
        this.idPemilik = idPemilik;
    }

    public String getIdKapal() {
        return idKapal;
    }

    public void setIdKapal(String idKapal) {
        this.idKapal = idKapal;
    }

    public String getNamaKapal() {
        return namaKapal;
    }

    public void setNamaKapal(String namaKapal) {
        this.namaKapal = namaKapal;
    }
}
